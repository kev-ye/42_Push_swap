#include "common.h"

void	lstclear(t_data **lst)
{
	t_data	*tmp;

	tmp = NULL;
	while (*lst)
	{
		tmp = (*lst)->next;
		lstdelone(*lst);
		*lst = tmp;
	}
	*lst = NULL;
}
